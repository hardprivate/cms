            <h3>Как настроить?</h3>
            <p>Есть два способа настройки: с использованием прокси и с использованием VPN.</p>
                <p><b>Рекомендуемый способ для компьютера</b> — с использованием <b>прокси</b>. Этот способ поддерживается всеми десктопными браузерами, шифрует соединение в Firefox и браузерах, основанных на Chromium (Google Chrome, Яндекс.Браузер, Opera), но из мобильных устройств поддерживается только в iOS.</p>
                <p><b>Используйте VPN на Android, iOS и маршрутизаторах</b>, а также если вы хотите получить доступ из программы, которая не поддерживает работу с прокси.</p>
