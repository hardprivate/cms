            <h3 class="align-center">Прокси</h3>
            <p>Предпочтительный способ для компьютеров.</p>
            <p><b>Chrome, Яндекс.Браузер, Vivaldi:</b><br>Используйте
            <a target="_blanck" href="https://chrome.google.com/webstore/detail/обход-блокировок-рунета/npgcnondjocldhldegnakemclmfkngch">расширение
            «Обход блокировок Рунета»</a><br><b>или</b><br>Настройте следующим образом:
            Настройки → Показать дополнительные настройки → Изменить настройки прокси-сервера → Настройка сети.<br>
            Вставить ссылку <a target="_blanck" href="https://antizapret.prostovpn.org/proxy.pac">https://antizapret.prostovpn.org/proxy.pac</a>
            в строку "Использовать сценарий автоматической настройки".</p>

            <p><b>Firefox:</b><br>Меню → Настройки → Кнопка «настроить…» пункта «прокси-сервер».<br>
             Вставить ссылку <a target="_blanck" href="https://antizapret.prostovpn.org/proxy.pac">https://antizapret.prostovpn.org/proxy.pac</a>
            в строку "URL автоматической настройки прокси".</p>

            <p><b>Opera:</b><br>
            Настройки → Браузер → Сеть → Изменить настройки прокси-сервера.<br>
            Вставить ссылку <a target="_blanck" href="https://antizapret.prostovpn.org/proxy.pac">https://antizapret.prostovpn.org/proxy.pac</a>
            в строку "Использовать сценарий автоматической настройки".</p>

            <p><b>Internet Explorer:</b><br>Сервис → Свойства обозревателя (браузера) → Безопасность → выделить «Местная интрасеть»
            → сайты → снять галку «Все сайты, подключение к которым выполняется в обход прокси-сервера»<br>
            Сервис → Свойства обозревателя (браузера) → Подключения → Настройка сети.<br>
            Вставить ссылку <a target="_blanck" href="https://antizapret.prostovpn.org/proxy.pac">https://antizapret.prostovpn.org/proxy.pac</a>
            в строку "Использовать сценарий автоматической настройки".</p>

            <p><b>iPhone/iPad:</b><br><i>Работает только для определенной Wi-Fi-сети. Рекомендуется использовать VPN.</i><br>
            <p>Настройки → Wi-Fi → Нажать "i" напротив сети → Установить «HTTP-прокси» в «Авто»,
            вставить ссылку <a target="_blanck" href="https://antizapret.prostovpn.org/proxy.pac">https://antizapret.prostovpn.org/proxy.pac</a></p></p>

            <p><b>Android 5.0+:</b><br><i>Работает только для определенной Wi-Fi-сети. Рекомендуется использовать VPN.</i>
            <p>Настройки → Wi-Fi → Нажать и удерживать активную Wi-Fi сеть → Изменить настройки →
            Установить «Прокси» в «Proxy Auto-Configuration», вставить ссылку <a target="_blanck" href="https://antizapret.prostovpn.org/proxy.pac">https://antizapret.prostovpn.org/proxy.pac</a></p></p>
        </div>
